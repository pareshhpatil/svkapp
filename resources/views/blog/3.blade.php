<div class="section mt-2">
  <h3>1. Alibaug</h3>
  <p>
    Alibaug is a coastal town located about 120 kilometers from Mumbai. It is a popular weekend getaway
    known for its beaches, backwaters, and fishing villages.
  <figure>
    <img src="/assets/img/blog/11.jpg" alt="image" class="imaged img-fluid">
  </figure>
  <ul>
    <li>
      <h4>Alibaug Beach</h4>
      <p>Alibaug Beach is one of the most popular beaches in Alibaug. It is a long, sandy beach with clear
        blue waters. The beach is perfect for swimming, sunbathing, and water sports.</p>
    </li>
    <li>
      <h4>Alibaug Backwaters
      </h4>
      <p>
        Alibaug Backwaters are a network of canals and creeks that wind their way through the town of
        Alibaug. The backwaters are a popular spot for boating, kayaking, and fishing.
      </p>
    </li>
    <li>
      <h4>Alibaug Fishing Village
      </h4>
      <p>
        Alibaug Fishing Village is a small village located on the coast of Alibaug. The village is home
        to a number of fishing families. Visitors can watch the fishermen at work and learn about the
        traditional fishing methods used in Alibaug.
      </p>
    </li>
  </ul>
  </p>


  <h3>2. Matheran</h3>
  <p>
    Matheran is a hill station located about 80 kilometers from Mumbai. It is a car-free town that can only
    be reached by foot or by a toy train. Matheran is known for its lush forests, wildlife, and stunning
    views.


  </p>
  <figure>
    <img src="/assets/img/blog/12.jpg" alt="image" class="imaged img-fluid">
  </figure>
  <ul>
    <li>
      <h4>Matheran Forests

      </h4>
      <p>Matheran is home to a number of lush forests. The forests are home to a variety of wildlife,
        including monkeys, deer, and birds. Visitors can go for walks in the forests, go birdwatching,
        or simply relax and enjoy the peace and quiet.</p>
    </li>
    <li>
      <h4>Matheran Wildlife


      </h4>
      <p>
        Matheran is home to a variety of wildlife, including monkeys, deer, and birds. Visitors can go
        for walks in the forests, go birdwatching, or simply relax and enjoy the peace and quiet.
      </p>
    </li>
    <li>
      <h4>Matheran Views


      </h4>
      <p>
        Matheran offers stunning views of the surrounding countryside. Visitors can enjoy the views from
        the top of the hills, or from one of the many viewpoints located throughout the town.
      </p>
    </li>
  </ul>
  <h3>3. Lonavala and Khandala</h3>
  <p>
    Lonavala and Khandala are two hill stations located about 100 kilometers from Mumbai. They are popular
    tourist destinations known for their scenic beauty, waterfalls, and forts.

  </p>
  <figure>
    <img src="/assets/img/blog/13.jpg" alt="image" class="imaged img-fluid">
  </figure>
  <ul>
    <li>
      <h4>Lonavala Waterfalls
      </h4>
      <p>Lonavala is home to a number of waterfalls, including the Karla Falls, the Rajmachi Falls, and
        the Latkan Falls. The waterfalls are a popular spot for swimming, picnicking, and hiking.

      </p>
    </li>
    <li>
      <h4>Lonavala Forts
      </h4>
      <p>
        Lonavala is also home to a number of forts, including the Lohagad Fort, the Torna Fort, and the
        Visapur Fort. The forts offer stunning views of the surrounding countryside and are a popular
        spot for hiking and trekking.
      </p>
    </li>
    <li>
      <h4>Khandala Waterfalls
      </h4>
      <p>
        Khandala is also home to a number of waterfalls, including the Dudhsagar Falls, the Andharban
        Falls, and the Chandni Falls. The waterfalls are a popular spot for swimming, picnicking, and
        hiking.
      </p>
    </li>
  </ul>

