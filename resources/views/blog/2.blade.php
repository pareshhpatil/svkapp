    <div class="lead">
      Here are just a few of the many things you can do in Mumbai. With its endless attractions and
      activities, Mumbai is a city that has something to offer everyone.
    </div>
  </div>

  <div class="section mt-2">
    <h3>1. Visit the Gateway of India</h3>
    <p>
      The Gateway of India is one of the most iconic landmarks in Mumbai. It was built in 1924 to commemorate
      the visit of King George V and Queen Mary to India. The Gateway is a beautiful structure made of white
      stone and is located on the waterfront in South Mumbai. It is a popular tourist destination and a great
      place to take photos.
    </p>
    <figure>
      <img src="/assets/img/blog/1.jpg" alt="image" class="imaged img-fluid">
    </figure>

    <h3>2. Take a walk along Marine Drive</h3>
    <p>
      Marine Drive is another iconic Mumbai landmark. It is a long seafront promenade that stretches for over
      10 kilometers. Marine Drive is a great place to take a walk, go for a run, or simply relax and enjoy the
      views of the Arabian Sea.
    </p>
    <figure>
      <img src="/assets/img/blog/2.png" alt="image" class="imaged img-fluid">
    </figure>
    <h3>3. Explore the Chhatrapati Shivaji Maharaj Vastu Sangrahalaya</h3>
    <p>
      The Chhatrapati Shivaji Maharaj Vastu Sangrahalaya, also known as the Prince of Wales Museum, is one of
      the largest and most important museums in India. It houses a vast collection of art and artifacts from
      all over the world, including ancient Egyptian mummies, Mughal miniatures, and Chinese porcelain. </p>
    <figure>
      <img src="/assets/img/blog/3.jpg" alt="image" class="imaged img-fluid">
    </figure>
    <h3>4. Visit the Elephanta Caves</h3>
    <p>
      The Elephanta Caves are a UNESCO World Heritage Site located on an island just off the coast of Mumbai.
      The caves are home to a stunning collection of rock-cut sculptures dating back to the 7th century. The
      sculptures depict scenes from Hindu mythology and are considered to be some of the finest examples of
      Indian art.

    </p>
    <figure>
      <img src="/assets/img/blog/4.jpg" alt="image" class="imaged img-fluid">
    </figure>
    <h3>5. Shop at Colaba Causeway</h3>
    <p>
      Colaba Causeway is a bustling market located in the heart of Mumbai. It is a great place to find
      everything from souvenirs to designer clothes. Be sure to bargain hard!

    </p>
    <figure>
      <img src="/assets/img/blog/5.jpg" alt="image" class="imaged img-fluid">
    </figure>
    <h3>6. Try the street food</h3>
    <p>
      Mumbai is home to some of the best street food in the world. Be sure to try some of the local favorites,
      such as pav bhaji, vada pav, and pani puri.

    </p>
