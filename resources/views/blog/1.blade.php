  <h3>1. Always wear a seatbelt.</h3>
  <p>
    Seatbelts save lives. In fact, they reduce the risk of death in a car crash by 50%. Make sure you and
    all passengers are buckled up before you start driving.
  </p>


  <h3>2. Don't drink and drive.</h3>
  <p>
    Alcohol impairs your judgment and coordination, making it very dangerous to drive. If you've been
    drinking, don't get behind the wheel. Call a cab or a friend for a ride.
  </p>

  <h3>3. Obey the speed limit.</h3>
  <p>
    Speeding is a major factor in car accidents. The faster you're going, the less time you have to react to
    hazards. Stick to the speed limit and avoid getting a ticket. </p>

  <h3>4. Don't text and drive.</h3>
  <p>
    Texting while driving is incredibly dangerous. It takes your eyes off the road, your hands off the
    wheel, and your mind off of driving. If you need to send a text, pull over to a safe location.

  </p>

  <h3>5. Be aware of your surroundings.</h3>
  <p>
    Pay attention to what's going on around you at all times. Watch out for other cars, pedestrians,
    cyclists, and animals. Be prepared to react to anything that might happen.

  </p>

  <h3>6. Don't drive drowsy.</h3>
  <p>
    Driving drowsy is just as dangerous as driving drunk. If you're feeling tired, pull over and take a nap.
    It's better to be safe than sorry.



  </p>
  <h3>7. Be prepared for the unexpected.</h3>
  <p>
    It's always a good idea to be prepared for the unexpected when you're driving. This means having a
    first-aid kit, a flashlight, and a cell phone in your car. It also means knowing how to change a tire
    and how to call for help in an emergency.

  </p>

